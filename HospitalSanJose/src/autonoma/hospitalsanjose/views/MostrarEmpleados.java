    private void btnActualizarEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarEActionPerformed
        int fila = this.TableEmpleados.getSelectedRow();
        if (fila >= 0){
            Empleado e = this.listaEmpleados.get(fila);
            ActualizarEmpleado ventanaActualizarE = new ActualizarEmpleado(this.ventanaPrincipal,true,hospital,this,e); 
            ventanaActualizarE.setVisible(true);
        }
        else {
            JOptionPane.showMessageDialog(this, "Porfavor seleccione el empleado que desea actualizar");
        }
    }//GEN-LAST:event_btnActualizarEActionPerformed

    private void btnEliminarEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarEActionPerformed
        int fila = this.TableEmpleados.getSelectedRow();
        if (fila >= 0){
            Empleado e = this.listaEmpleados.get(fila);
            int option = JOptionPane.showConfirmDialog(this, "Â¿Desea eliminar el empleado"+e.getNombre()+ " de forma permanente?");
            if (option == 0){
            this.hospital.eliminarEmpleado(e.getId());
            this.listaEmpleados=this.hospital.getNomina().getlistaEmpleados();
            this.llenarTabla();
            JOptionPane.showMessageDialog(this, "El empleado "+e.getNombre()+" fue eliminado de forma exitosa");
            
            }
        }
        else {
            JOptionPane.showMessageDialog(this, "Porfavor seleccione el empleado que desea eliminar");
        }
    }//GEN-LAST:event_btnEliminarEActionPerformed
    
    public void llenarTabla (){
         DefaultTableModel modelDefault = new DefaultTableModel (new String[]{"Nombre","NÃºmero Documento", "Edad","Salario Base","especialidad", "Numero Horas Trabajadas", "Area de Trabajo"},this.listaEmpleados.size());
        this.TableEmpleados.setModel(modelDefault); 
        TableModel dataModel = this.TableEmpleados.getModel();
        for (int i = 0; i < this.listaEmpleados.size(); i++) {
            Empleado e = this.listaEmpleados.get(i);
            
            dataModel.setValueAt(e.getNombre(),i,0);
            dataModel.setValueAt(e.getNumeroDocumento(),i,1);
            dataModel.setValueAt(e.getEdad(),i,2);
            dataModel.setValueAt(e.getSalarioBase(),i,3);
            
        }
}
    

