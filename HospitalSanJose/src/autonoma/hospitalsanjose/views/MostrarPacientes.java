    public void llenarTabla (){
         DefaultTableModel modelDefault = new DefaultTableModel (new String[]{"Nombre","Número Documento", "Edad","Correo Electrónico","Telefono", "Estado"},this.listaPacientes.size());
        this.TablePacientes.setModel(modelDefault); 
        TableModel dataModel = this.TablePacientes.getModel();
        for (int i = 0; i < this.listaPacientes.size(); i++) {
            Paciente paciente = this.listaPacientes.get(i);
            dataModel.setValueAt(paciente.getNombre(),i,0);
            dataModel.setValueAt(paciente.getNumDocumento(),i,1);
            dataModel.setValueAt(paciente.getEdad(),i,2);
            dataModel.setValueAt(paciente.getCorreoElectronico(),i,3);
            dataModel.setValueAt(paciente.getTelefono(),i,4);
            dataModel.setValueAt(paciente.getEstado(),i,5);
        }
}

