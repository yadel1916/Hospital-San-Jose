    private void btnEliminarPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarPMouseClicked
        int fila = this.TablePacientes.getSelectedRow();
        if (fila >= 0){
            Paciente p = this.listaPacientes.get(fila);
            int option = JOptionPane.showConfirmDialog(this, "¿Desea eliminar el paciente"+p.getNombre()+ " de forma permanente?");
            if (option == 0){
            this.hospital.eliminarPaciente(p.getId());
            this.listaPacientes=this.hospital.getListaPacientes();
            this.llenarTabla();
            JOptionPane.showMessageDialog(this, "El paciente "+p.getNombre()+" fue eliminado de forma exitosa");
            
            }
        }
        else {
            JOptionPane.showMessageDialog(this, "Porfavor seleccione el paciente que desea eliminar");
        }
    }//GEN-LAST:event_btnEliminarPMouseClicked

