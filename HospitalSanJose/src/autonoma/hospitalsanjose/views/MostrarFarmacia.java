    private void btnBuscarMMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarMMouseClicked
        String nombre = this.txtBuscarM.getText();
        Medicamento m = this.hospital.buscarMedicamentoNombre(nombre);
        if(m== null){
            JOptionPane.showMessageDialog(this, "el medicamento con nombre: " + nombre + " no se encuentra registrado" );
        }
        else {
            JOptionPane.showMessageDialog(this, "El medicamento con nombre: "+ nombre + " se encuentra registrado");
        }
        this.txtBuscarM.setText("");
    }//GEN-LAST:event_btnBuscarMMouseClicked

    private void btnActualizarMMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnActualizarMMouseClicked
        int fila = this.TableFarmacia.getSelectedRow();
        if (fila >= 0){
            Medicamento m = this.listaMedicamentos.get(fila);
            ActualizarMedicamento ventanaActualizarM = new ActualizarMedicamento(this.ventanaPrincipal,true,hospital,this,m);
            ventanaActualizarM.setVisible(true);
        }
        else {
            JOptionPane.showMessageDialog(this, "Porfavor seleccione el medicamento que desea actualizar");
        }
    }//GEN-LAST:event_btnActualizarMMouseClicked

    private void btnEliminarMMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarMMouseClicked
        int fila = this.TableFarmacia.getSelectedRow();
        if (fila >= 0){
            Medicamento m = this.listaMedicamentos.get(fila);
            int option = JOptionPane.showConfirmDialog(this, "¬øDesea eliminar el medicamento "+m.getNombre()+ " de forma permanente?");
            if (option == 0){
                this.hospital.eliminarMedicamento(m.getNombre());
                this.listaMedicamentos=this.hospital.getFarmacia().getInventario().getlistaMedicamentos();
                this.llenarTabla();
                JOptionPane.showMessageDialog(this, "El medicamento "+m.getNombre()+" fue eliminado de forma exitosa");

            }
        }
        else {
            JOptionPane.showMessageDialog(this, "Porfavor seleccione el medicamento que desea eliminar");
        }
    }//GEN-LAST:event_btnEliminarMMouseClicked

