    private void btnActualizarCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarCActionPerformed
        try{
            String fechaConsulta = this.txtFechaC.getText();         
            double costoConsulta = Double.parseDouble(this.txtCostoConsulta.getText());
             
           
            this.cita.setFechaConsulta(fechaConsulta);
            this.cita.setValorConsulta(costoConsulta);
           
            
            this.hospital.actualizarCita(this.cita);
            JOptionPane.showMessageDialog(this," La cita del paciente:  "+cita.getPaciente().getNombre()+" fue actualizada exitosamente");
            this.mostrarCitas.llenarTabla();
            this.dispose();
 
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this,"Porfavor ingrese un n√∫mero en el campo de texto");
            this.txtCostoConsulta.setText("");
            
            
        }
    }//GEN-LAST:event_btnActualizarCActionPerformed

