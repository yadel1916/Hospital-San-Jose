    private void btnAgregarPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarPActionPerformed
        try{
            ArrayList <Empleado> listaEmpleados=hospital.getNomina().getlistaEmpleados();
            
            int num_random=(int)(Math.random()*listaEmpleados.size());
            
            String nombre = this.paciente.getNombre();
            
            String fechaconsulta = this.txtFechaC.getText();
            double costoConsulta = Double.parseDouble(this.txtCostoConsulta.getText());
            
            

            Cita cita = new Cita(this.paciente,(EmpleadoAreaSalud)listaEmpleados.get(num_random),fechaconsulta,costoConsulta);
            if(this.hospital.registrarCita(cita)){
                JOptionPane.showMessageDialog(this," Paciente "+nombre+" se asigno la cita exitosamente");
                this.dispose();

            }
            else {
                JOptionPane.showMessageDialog(this," Ha ocurrido un error, el paciente no se pudo agendar cita ");
                this.dispose();

            }
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this,"Porfavor ingrese un n√∫mero en el campo de texto");
            this.txtCostoConsulta.setText("");
        }

    }//GEN-LAST:event_btnAgregarPActionPerformed

