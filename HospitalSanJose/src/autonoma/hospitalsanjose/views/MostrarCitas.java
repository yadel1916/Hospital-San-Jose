    private void btnActualizarCMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnActualizarCMouseClicked
        int fila = this.TableCitas.getSelectedRow();
        if (fila >= 0){
            Cita c = this.listaCitas.get(fila);
            ActualizarCita ventanaActualizarC = new ActualizarCita(null,true,hospital,this,c); 
            ventanaActualizarC.setVisible(true);
        }
        else {
            JOptionPane.showMessageDialog(this, "Porfavor seleccione el paciente que desea actualizar");
        }
    }//GEN-LAST:event_btnActualizarCMouseClicked

    private void btnEliminarCMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarCMouseClicked
        int fila = this.TableCitas.getSelectedRow();
        if (fila >= 0){
            Cita c = this.listaCitas.get(fila);
            int option = JOptionPane.showConfirmDialog(this, "¿Desea eliminar la cita para el paciente:  "+c.getPaciente().getNombre()+ " de forma permanente?");
            if (option == 0){
                this.hospital.eliminarCita(c);
                this.listaCitas=this.hospital.getListaCitas();
                this.llenarTabla();
                JOptionPane.showMessageDialog(this, "El la cita para el paciente:  "+c.getPaciente().getNombre()+" fue eliminada de forma exitosa");

            }
        }
        else {
            JOptionPane.showMessageDialog(this, "Porfavor seleccione la cita que desea eliminar");
        }
    }//GEN-LAST:event_btnEliminarCMouseClicked

    private void btnBuscarCMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarCMouseClicked
        String fechaConsulta = this.txtBuscarM.getText(); 
        Cita c = this.hospital.buscarCitaFechaConsulta(fechaConsulta);
        if(c == null){
            JOptionPane.showMessageDialog(this, "la cita  con fecha de consulta: " + fechaConsulta + " no se encuentra registrada" );
        }
        else {
            JOptionPane.showMessageDialog(this, "El paciente con nombre "+ c.getPaciente().getNombre() +   " tiene una cita asiganda para la fecha : "+ fechaConsulta);
        }
        this.txtBuscarM.setText("");
    }//GEN-LAST:event_btnBuscarCMouseClicked

