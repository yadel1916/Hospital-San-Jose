    /**
     * @param args the command line arguments
     */
    public void llenarTabla (){
         DefaultTableModel modelDefault = new DefaultTableModel (new String[]{"Nombre Paciente","Medico", "Fecha Consulta","Costo Consulta"},this.listaCitas.size());
        this.TableCitas.setModel(modelDefault); 
        TableModel dataModel = this.TableCitas.getModel();
        for (int i = 0; i < this.listaCitas.size(); i++) {
            Cita c = this.listaCitas.get(i);
            dataModel.setValueAt(c.getPaciente().getNombre(),i,0);
            dataModel.setValueAt(c.getMedicoConsulta().getNombre(),i,1);
            dataModel.setValueAt(c.getFechaConsulta(),i,2);
            dataModel.setValueAt(c.getValorConsulta(),i,3);       
        }
}

