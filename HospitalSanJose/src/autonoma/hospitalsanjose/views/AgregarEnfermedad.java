    private void btnAgregarEnfermedadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarEnfermedadActionPerformed
        try { 
            
        
            String nombrePa = this.txtNombrePaciente.getText();
            String nombre = this.txtNombre.getText();
            String descripcion = this.txtDescripcionM.getText();
           

            Enfermedad em = new Enfermedad(nombre,nombrePa,descripcion);
            if(this.paciente.agregarEnfermedad(em)){
                JOptionPane.showMessageDialog(this," La Enfermedad " + nombre +" fue agregada exitosamente");
                this.dispose();

            }
            else {
                JOptionPane.showMessageDialog(this," Ha ocurrido un error, la enfermeda no se pudo agregar ");
                this.dispose();

            }
        } catch (Exception e){
            System.out.println("error");
        }
    }//GEN-LAST:event_btnAgregarEnfermedadActionPerformed

