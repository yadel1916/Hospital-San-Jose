    private void btnActualizarEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarEActionPerformed
         try{
            String nombre = this.txtNombreE.getText();
            String numeroDocumento = this.txtNumeroDocumentoE.getText();
            int edad = Integer.parseInt(this.txtEdadE.getText());
            double salarioBase = Double.parseDouble(this.txtSalarioBase.getText());
//            String telefono = this.txtTelefonoP.getText();
//            String estado = this.txtEstadoP.getText();
            this.empleado.setNombre(nombre);
            this.empleado.setNumeroDocumento(numeroDocumento);
            this.empleado.setEdad(edad);
            this.empleado.setSalarioBase(salarioBase);
//            this.paciente.setTelefono(telefono);
//            this.paciente.setEstado(estado);
            this.hospital.actualizarEmpleado(this.empleado.getId(), this.empleado);
            JOptionPane.showMessageDialog(this," empleado "+nombre+" fue actualizado exitosamente");
            this.mostrarEmpleados.llenarTabla();
            this.dispose();
 
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this,"Porfavor ingrese un n√∫mero en el campo de texto");
            this.txtEdadE.setText("");
            this.txtSalarioBase.setText("");
        }
    }//GEN-LAST:event_btnActualizarEActionPerformed

