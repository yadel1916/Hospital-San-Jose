    private void btnEliminarPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarPMouseClicked
        int fila = this.TableEnfermedad.getSelectedRow();
        if (fila >= 0){
            Enfermedad em= this.listaEnfermedades.get(fila);
            int option = JOptionPane.showConfirmDialog(this, "Â¿Desea eliminar la enfermedad "+ em.getNombre() + " de forma permanente?");
            if (option == 0){
                this.paciente.eliminarEnfermedad(em.getNombre());
                this.listaEnfermedades = this.paciente.getEnfermedades();
                this.llenarTabla();
                JOptionPane.showMessageDialog(this, "La enfermedad "+ em.getNombre()+" fue eliminado de forma exitosa");

            }
        }
        else {
            JOptionPane.showMessageDialog(this, "Porfavor seleccione el paciente que desea eliminar");
        }
    }//GEN-LAST:event_btnEliminarPMouseClicked

